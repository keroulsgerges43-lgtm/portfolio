# Problem 3 - Book Price Average Checker



book1 = float(input("Enter the price of book 1: "))



book2 = float(input("Enter the price of book 2: "))



book3 = float(input("Enter the price of book 3: "))



average_price = (book1 + book2 + book3) / 3



print("Average book price:", average_price)



if average_price > 100:



    print("Too expensive.")



else:



    print("OK to Purchase")